export const sum = (num1: number, num2: number): number => {
    return(num1 + num2)
   };
   

export const multiply = (a: number, b: number): number => {
    return(a * b)
   }

export const isEven = (n: number): boolean => {
    if (n % 2 ===0){
        return true
    }
    else{
        return false
    }
   };
   